function initFilters() {
  addOptions(groupFilter, uniqueValues(rows, "group"));
  addOptions(activityFilter, uniqueValues(rows, "activity"));
}

function addOptions(selectEl, values) {
  values.forEach((value) => {
    const option = document.createElement("option");
    option.value = value;
    option.textContent = value;
    selectEl.append(option);
  });
}

function uniqueValues(items, field) {
  return [...new Set(items.map((item) => item[field]).filter(Boolean))].sort((a, b) =>
    a.localeCompare(b, "ru")
  );
}

function getFilteredRows() {
  const query = searchInput.value.trim().toLowerCase();
  const group = groupFilter.value;
  const activity = activityFilter.value;
  const status = statusFilter.value;

  return rows.filter((row) => {
    const searchHaystack = [
      row.articleNoRaw,
      row.name,
      row.group,
      row.activity,
      row.description,
      row.source,
      row.comment,
    ]
      .join(" ")
      .toLowerCase();

    const passesQuery = !query || searchHaystack.includes(query);
    const passesGroup = group === "all" || row.group === group;
    const passesActivity = activity === "all" || row.activity === activity;
    const passesStatus = status === "all" || row.status === status;

    return passesQuery && passesGroup && passesActivity && passesStatus;
  });
}

function render() {
  const filtered = getFilteredRows();
  renderMetrics(filtered);
  renderTable(filtered);
}

function renderMetrics(filteredRows) {
  const counts = {
    total: filteredRows.length,
    active: filteredRows.filter((row) => row.status === "ACTIVE").length,
    inactive: filteredRows.filter((row) => row.status === "INACTIVE").length,
    delete: filteredRows.filter((row) => row.status === "DELETE").length,
    rename: filteredRows.filter((row) => row.status === "RENAME").length,
  };

  metrics.innerHTML = `
    <article class="metric"><div class="label">Строк в выдаче</div><div class="value">${counts.total}</div></article>
    <article class="metric"><div class="label">Активные</div><div class="value">${counts.active}</div></article>
    <article class="metric"><div class="label">Неактуальные</div><div class="value">${counts.inactive}</div></article>
    <article class="metric"><div class="label">На удаление</div><div class="value">${counts.delete}</div></article>
    <article class="metric"><div class="label">На переименование</div><div class="value">${counts.rename}</div></article>
  `;
}

function renderTable(filteredRows) {
  tableBody.innerHTML = filteredRows
    .map(
      (row) => `
      <tr data-status="${row.status}">
        <td>${escapeHtml(row.articleNoRaw)}</td>
        <td>${escapeHtml(row.name)}</td>
        <td>${escapeHtml(row.group)}</td>
        <td>${escapeHtml(row.activity)}</td>
        <td>${escapeHtml(row.description)}</td>
        <td>${escapeHtml(row.source)}</td>
        <td>${escapeHtml(row.comment)}</td>
        <td><span class="badge ${statusClass(row.status)}">${statusLabel(row.status)}</span></td>
      </tr>`
    )
    .join("");
}

function escapeHtml(value) {
  return String(value || "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function toCsv(rowsToExport) {
  const headers = [
    "№ статьи",
    "Статья ДДС",
    "Группа",
    "Вид деятельности",
    "Описание",
    "Источник",
    "Комментарий",
    "Статус",
  ];

  const lines = rowsToExport.map((row) => [
    row.articleNoRaw,
    row.name,
    row.group,
    row.activity,
    row.description,
    row.source,
    row.comment,
    statusLabel(row.status),
  ]);

  return [headers, ...lines]
    .map((line) => line.map((cell) => `"${String(cell || "").replaceAll('"', '""')}"`).join(";"))
    .join("\n");
}

function downloadFilteredCsv() {
  const filtered = getFilteredRows();
  const csv = toCsv(filtered);
  const blob = new Blob([csv], { type: "text/csv;charset=utf-8;" });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");

  link.href = url;
  link.download = "dds-articles-filtered.csv";
  document.body.append(link);
  link.click();
  link.remove();

  URL.revokeObjectURL(url);
}
