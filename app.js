function parseRows(raw) {
  return raw
    .trim()
    .split("\n")
    .map((line, idx) => {
      const [articleNoRaw, name, group, activity, description, source, comment] = line
        .split("|")
        .map((cell) => cell.trim());

      return {
        id: idx + 1,
        articleNoRaw,
        name,
        group,
        activity,
        description,
        source,
        comment,
        status: resolveStatus(articleNoRaw, comment),
      };
    });
}

function resolveStatus(articleNoRaw, comment) {
  const raw = articleNoRaw.toLowerCase();
  const cmt = comment.toLowerCase();

  if (raw.startsWith("удалить")) {
    return "DELETE";
  }

  if (raw.startsWith("поменять")) {
    return "RENAME";
  }

  if (cmt.includes("не актуальный") || cmt.includes("неактуальный")) {
    return "INACTIVE";
  }

  return "ACTIVE";
}

function statusLabel(status) {
  if (status === "DELETE") return "Удалить";
  if (status === "RENAME") return "Переименовать";
  if (status === "INACTIVE") return "Неактуально";
  return "Активно";
}

function statusClass(status) {
  if (status === "DELETE") return "delete";
  if (status === "RENAME") return "rename";
  if (status === "INACTIVE") return "inactive";
  return "active";
}

